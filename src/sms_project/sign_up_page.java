    private void sign_upActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sign_upActionPerformed
        String userid = user_id.getText();
        String Name = name.getText();
        String pwd = password.getText();
        String sec_question = security_question.getText();
        String sec_answer = security_answer.getText();

        if (!userid.isEmpty() && !Name.isEmpty() && !pwd.isEmpty() && !sec_question.isEmpty() && !sec_answer.isEmpty()) {
            if (!(isExists(userid))) {
                try (Connection conn = JDBC.con(); PreparedStatement pst = conn.prepareStatement("sp_sms_admin_insert ?,?,?,?,?")) {
                    pst.setString(1, userid);
                    pst.setString(2, Name);
                    pst.setString(3, pwd);
                    pst.setString(4, sec_question);
                    pst.setString(5, sec_answer);

                    int result = pst.executeUpdate();
                    if (result == 1) {
                        JOptionPane.showMessageDialog(this, "Signed-Up");
                        this.setVisible(false);
                        new sign_in_page().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "Sign-Up Error");
                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Same ID Already Exixts");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Enter All Datas");
        }


    }//GEN-LAST:event_sign_upActionPerformed

